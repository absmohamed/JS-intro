// for..in loop is used if the index of the elements of the array is required.
let colorsOfRainbow = ['Red', 'Orange', 'Yellow', 'Green',
  'Blue', 'Indigo', 'Violet']

for (let color in colorsOfRainbow) {
  console.log(color)
}